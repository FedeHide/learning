#### Operators

#### Arithmetic Operators
# Addition
add = 10 + 5

# Subtraction
sub = 10 - 5

# Multiplication
mul = 10 * 5

# Division
div = 10 / 5

# Modulus
mod = 10 % 5

# Exponentiation
exp = 10 ** 5

# Floor Division
fdiv = 10 // 5

#!-----------------------------!#

#### Comparison Operators
# Equal
eq = 10 == 5

# Not Equal
neq = 10 != 5

# Greater Than
gt = 10 > 5

# Less Than
lt = 10 < 5

# Greater Than or Equal To
gte = 10 >= 5

# Less Than or Equal To
lte = 10 <= 5

#!-----------------------------!#

#### Logical Operators
# AND
and_op = True and False

# OR
or_op = True or False

# NOT
not_op = not True

#!-----------------------------!#

#### Identity Operators
a = [1, 2, 3]
b = [1, 2, 3]
c = a

# is
is_op = (a is b) # false

# is not
is_not_op = (a is not b) # true

#!-----------------------------!#

#### Membership Operators
# in
in_op = 10 in [1, 2, 3, 4, 5]

# not in
not_in_op = 10 not in [1, 2, 3, 4, 5]

#!-----------------------------!#

#### Bitwise Operators
# AND
bit_and = 10 & 5

# OR
bit_or = 10 | 5

# XOR
bit_xor = 10 ^ 5

# NOT
bit_not = ~10

# Left Shift
bit_lshift = 10 << 5

# Right Shift
bit_rshift = 10 >> 5

#!-----------------------------!#

#### Assignment Operators
# =
assign = 10

# +=
assign_add = 10
assign_add += 5

# -=
assign_sub = 10
assign_sub -= 5

# *=
assign_mul = 10
assign_mul *= 5

# /=
assign_div = 10
assign_div /= 5

# %=
assign_mod = 10
assign_mod %= 5

# **=
assign_exp = 10
assign_exp **= 5

# //=
assign_fdiv = 10
assign_fdiv //= 5

#!-----------------------------!#

#### Bitwise Assignment Operators
# &=
assign_bit_and = 10
assign_bit_and &= 5

# |=
assign_bit_or = 10
assign_bit_or |= 5

# ^=
assign_bit_xor = 10
assign_bit_xor ^= 5

# ~=
assign_bit_not = 10
assign_bit_not = ~assign_bit_not

# <<=
assign_bit_lshift = 10
assign_bit_lshift <<= 5

# >>=
assign_bit_rshift = 10
assign_bit_rshift >>= 5

#!-----------------------------!#

#### Ternary Operator
# Syntax: [on_true] if [expression] else [on_false]
ternary = "Greater" if 10 > 5 else "Lesser" # Greater

